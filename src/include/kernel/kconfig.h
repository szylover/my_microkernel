#pragma once

/*
 * kconfig.h — 内核编译期配置（集中管理）
 *
 * [WHY] 所有可调节的内核选项集中在此，不散落在各 .c 文件中。
 *   修改配置只需编辑这一个文件，重新 make iso 即可生效。
 *
 * 用法：在需要读取配置的 .c 文件中 #include "kconfig.h"
 *
 * ============================================================================
 * PMM 物理内存分配器后端
 * ============================================================================
 *
 *   0 = bitmap  — 位图分配器，简单稳定
 *   1 = buddy   — 伙伴系统，支持多阶分配
 */
#define KCONFIG_PMM_BACKEND  0

/*
 * ============================================================================
 * kmalloc 内核堆后端
 * ============================================================================
 *
 *   0 = first_fit  — 空闲链表（双向链表 + 合并），简单通用
 *   1 = slab       — slab 分配器（固定大小 cache + bitmap），配合 buddy 更高效
 */
#define KCONFIG_HEAP_BACKEND  0

/*
 * ============================================================================
 * kmalloc 初始堆大小
 * ============================================================================
 *
 * 初始映射的物理页数（每页 4KiB）。后端按需 grow 扩展。
 */
#define KCONFIG_HEAP_INITIAL_PAGES  16

/*
 * ============================================================================
 * VMA 后端
 * ============================================================================
 *
 *   0 = sorted-array  — 排序数组，简单稳定，早期默认
 *   1 = rbtree         — 红黑树，O(log n) 全操作（Linux 2.4~6.0 方案）
 *   2 = maple-tree     — B+ tree 变体，高扇出缓存友好（Linux 6.1+ 方案）
 */
#define KCONFIG_VMA_BACKEND  0

/*
 * ============================================================================
 * 系统调用后端
 * ============================================================================
 *
 *   0 = int0x80   — 传统软中断方式（IDT[0x80] DPL=3），简单可靠
 *   1 = sysenter  — Intel 快速系统调用（MSR 配置），低延迟
 */
#define KCONFIG_SYSCALL_BACKEND  0
