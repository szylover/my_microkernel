; tss_flush.asm (NASM)
;
; 学习级说明：
; - jump_to_ring3：构造 iret 帧，从 Ring 0 跳转到 Ring 3
;
; ============================================================================
; [WHY] 为什么需要用 iret 跳转到 Ring 3？
; ============================================================================
;
; x86 没有"直接降级到 Ring 3"的指令。唯一的方式是利用 iret：
; iret 原本用于从中断/异常返回，它会从栈上弹出 EIP/CS/EFLAGS，
; 如果检测到目标 CS 的 RPL != 当前 CPL，还会弹出 ESP/SS。
;
; 我们的技巧：在 Ring 0 手动构造一个"假的中断返回帧"，
; 把 CS 设为用户代码段（RPL=3），SS 设为用户数据段（RPL=3），
; 然后执行 iret。CPU 会以为自己是从 Ring 3 的中断中返回，
; 从而切换到 Ring 3。
;
; ============================================================================
; [CPU STATE] iret 帧布局（栈从高地址到低地址）
; ============================================================================
;
; 当目标 CS.RPL > 当前 CPL 时（跨特权级返回），iret 按以下顺序弹栈：
;
;   高地址 ┌─────────────┐  ← 先 push（后弹出）
;          │   SS (user)  │  用户态栈段 0x23
;          ├─────────────┤
;          │  ESP (user)  │  用户态栈顶
;          ├─────────────┤
;          │   EFLAGS     │  标志寄存器（保持 IF=1）
;          ├─────────────┤
;          │  CS (user)   │  用户态代码段 0x1B
;          ├─────────────┤
;   低地址 │   EIP        │  跳转目标地址  ← 先弹出
;          └─────────────┘
;
; iret 弹出顺序：EIP → CS → EFLAGS → ESP → SS
;

bits 32

; ============================================================================
; 段选择子常量（与 gdt.h 保持一致）
; ============================================================================
USER_CODE_SEL equ 0x1B     ; GDT[3] | RPL=3  (用户代码段)
USER_DATA_SEL equ 0x23     ; GDT[4] | RPL=3  (用户数据段)

global jump_to_ring3

; ============================================================================
; void jump_to_ring3(uint32_t eip, uint32_t esp3);
; ============================================================================
;
; cdecl 调用约定：
;   [esp + 4] = eip  (用户态入口地址)
;   [esp + 8] = esp3 (用户态栈顶)
;
; [CPU STATE] 执行后：
;   CPL: 0 → 3
;   CS:  KERNEL_CODE(0x08) → USER_CODE(0x1B)
;   SS:  KERNEL_DATA(0x10) → USER_DATA(0x23)
;   DS/ES/FS/GS: → USER_DATA(0x23)
;   EIP: → 参数 eip
;   ESP: → 参数 esp3
;   EFLAGS.IF: 保持 1（中断打开，键盘/定时器仍工作）
;
; [CRITICAL] 此函数不会返回！
;
jump_to_ring3:
    ; 取出参数
    mov eax, [esp + 4]     ; eax = eip (用户态入口)
    mov ecx, [esp + 8]     ; ecx = esp3 (用户态栈顶)

    ; ----------------------------------------------------------------
    ; [WHY] 先切换 DS/ES/FS/GS 到用户数据段
    ;
    ; 如果不切换，iret 后 DS 仍指向内核数据段（DPL=0），
    ; 但 CPL 已经是 3，用户态代码访问 DS 会因为
    ; max(CPL=3, RPL) > DPL=0 而触发 #GP。
    ; ----------------------------------------------------------------
    mov dx, USER_DATA_SEL
    mov ds, dx
    mov es, dx
    mov fs, dx
    mov gs, dx

    ; ----------------------------------------------------------------
    ; 构造 iret 帧（按从高到低的顺序 push）
    ; ----------------------------------------------------------------

    ; SS (用户数据段，RPL=3)
    push dword USER_DATA_SEL

    ; ESP (用户态栈顶)
    push ecx

    ; EFLAGS — 读取当前值并确保 IF=1
    ;
    ; [WHY] pushfd 保存当前 EFLAGS，可能 IF=0（关中断上下文）。
    ; 强制设 bit 9（IF），确保用户态中断打开——
    ; 否则键盘和定时器中断不触发，系统假死。
    ;
    ; [BITFIELDS] EFLAGS bit 9 = IF (Interrupt Flag)
    pushfd
    or dword [esp], 0x200   ; 确保 IF=1

    ; CS (用户代码段，RPL=3)
    push dword USER_CODE_SEL

    ; EIP (用户态入口地址)
    push eax

    ; ----------------------------------------------------------------
    ; [CPU STATE] 此时栈布局（从 ESP 向上）：
    ;   [ESP+0]  = EIP
    ;   [ESP+4]  = CS  (0x1B)
    ;   [ESP+8]  = EFLAGS (IF=1)
    ;   [ESP+12] = ESP3
    ;   [ESP+16] = SS  (0x23)
    ;
    ; iret 检测到 CS.RPL=3 > CPL=0 → 跨特权级返回
    ; ----------------------------------------------------------------
    iret

; Mark stack as non-executable
section .note.GNU-stack noalloc noexec nowrite progbits
